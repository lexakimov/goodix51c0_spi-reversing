def process_raw_packet(full_packet):
    full_packet = bytes.fromhex(full_packet)
    if len(full_packet) != 2413:
        raise ValueError(f"Нужно 2413 байт, сейчас {len(full_packet)}")

    # отрезаем заголовок
    no_header = full_packet[8:]

    if len(no_header) != 2405:
        raise ValueError(f"Нужно 2405 байт, сейчас {len(no_header)}")

    # отрезаем no_checksum
    no_checksum = no_header[:-1]

    if len(no_checksum) != 2404:
        raise ValueError(f"Нужно 2404 байт, сейчас {len(no_checksum)}")

    payload = no_checksum[:-4]
    crc_sum = no_checksum[-4:]

    if len(payload) != 2400:
        raise ValueError(f"Нужно 2400 байт, сейчас {len(payload)}")

    if len(crc_sum) != 4:
        raise ValueError(f"Нужно 2400 байт, сейчас {len(crc_sum)}")

