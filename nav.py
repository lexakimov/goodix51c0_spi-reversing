def check_crc(payload: bytes) -> None:
    if len(payload) < 4:
        raise ValueError("payload слишком короткий для CRC")

    crc_sum = payload[-4:]
    exp_crc = int.from_bytes(crc_sum, "big")

    crc32_func = crcmod.predefined.mkCrcFun("crc-32-mpeg")
    act_crc = crc32_func(payload[:-4]) & 0xFFFFFFFF
    act_crc = ((act_crc & 0xFFFF) << 16) | (act_crc >> 16)

    if act_crc != exp_crc:
        print("expected:", hex(exp_crc))
        print("  actual:", hex(act_crc))
        raise ValueError("Контрольные суммы не совпадают")


def ChicagoHUDataRegroup(src: bytes, size: int) -> List[int]:
    if size % 6 != 0:
        raise ValueError("size должен быть кратен 6")
    if len(src) < size:
        raise ValueError("src короче size")

    out = [0] * ((size // 6) * 4)
    out_pos = 0

    for i in range(0, size, 6):
        b0, b1, b2, b3, b4, b5 = src[i:i + 6]

        p0 = ((b0 & 0x0F) << 8) | b1
        p1 = (b3 << 4) | (b0 >> 4)
        p2 = ((b5 & 0x0F) << 8) | b2
        p3 = (b4 << 4) | (b5 >> 4)

        if size == 0x960:
            out[out_pos] = p0
            out[out_pos + 1] = p1
            out[out_pos + 2] = p2
            out[out_pos + 3] = p3
        else:
            n0, n1, n2, n3 = out_pos, out_pos + 1, out_pos + 2, out_pos + 3
            out[((n0 % 0x40) * 0x50) + (n0 // 0x40)] = p0
            out[((n1 % 0x40) * 0x50) + (n1 // 0x40)] = p1
            out[((n2 % 0x40) * 0x50) + (n2 // 0x40)] = p2
            out[((n3 % 0x40) * 0x50) + (n3 // 0x40)] = p3

        out_pos += 4

    return out


def _u16_list_to_le_bytes(values: List[int]) -> bytes:
    out = bytearray(len(values) * 2)
    for i, v in enumerate(values):
        out[2 * i] = v & 0xFF
        out[2 * i + 1] = (v >> 8) & 0xFF
    return bytes(out)


def chicagoHUget_navdata(packet: bytes, packet_len: int, nav_data_size: int) -> Tuple[bool, bytes]:
    if packet_len < 4 or nav_data_size <= 0 or len(packet) < packet_len:
        return False, b""

    frame = packet[:packet_len]

    try:
        check_crc(frame)
    except ValueError:
        return False, b""

    regrouped = ChicagoHUDataRegroup(frame, 0x960)
    tmp = _u16_list_to_le_bytes(regrouped)

    out = tmp[:nav_data_size]
    if len(out) < nav_data_size:
        out += b"\x00" * (nav_data_size - len(out))

    return True, out


