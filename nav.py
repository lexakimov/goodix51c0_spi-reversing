def visualize_goodix_2400(packed2400, shape=(40, 40), cmap="gray", contrast="percentile"):
    if isinstance(packed2400, (bytes, bytearray, memoryview)):
        b = np.frombuffer(packed2400, dtype=np.uint8)
    else:
        b = np.asarray(packed2400, dtype=np.uint8).reshape(-1)

    # [data_from_device]     recvd data cmd-len: 0x50-2410
    # [chicagoHUget_navdata] 2404 2404 3200
    # [ChicagoHUDataRegroup] Regroup pixels:1600
    # [gf_savebaseTofile]    nav buffer size:3200 buffer size:10240

    # 1111 1111  1111 1111  1111 1111 (8 bit * 3 = 24 bit)
    # 1111 1111 1111   1111 1111 1111 (12 bit * 2 = 24 bit)

    # 80  10
    # 64  08

    #  40 x 32 = 1280
    #  50 x 40 = 2000

    # col:80 x row:64 = 5120 pixels
    # 5120 * 1.5 = 7680 - нужная длина данных



    if b.size != 2400:
        raise ValueError(f"Ожидалось 2400 байт, получено {b.size}")

    # 3 байта -> 2 пикселя (12-bit little-endian nibble packing)
    t = b.reshape(-1, 3).astype(np.uint16)
    p0 = t[:, 0] | ((t[:, 1] & 0x0F) << 8)
    p1 = (t[:, 1] >> 4) | (t[:, 2] << 4)

    pixels = np.empty(t.shape[0] * 2, dtype=np.uint16)
    pixels[0::2] = p0
    pixels[1::2] = p1  # всего 1600 пикселей

    if shape[0] * shape[1] != pixels.size:
        raise ValueError(f"shape={shape} не подходит для {pixels.size} пикселей")

    img = pixels.reshape(shape)

    if contrast == "percentile":
        lo, hi = np.percentile(img, [1, 99])
        disp = np.clip((img - lo) / (hi - lo + 1e-9), 0, 1)
    else:
        disp = (img - img.min()) / (img.max() - img.min() + 1e-9)

    plot.figure(figsize=(6, 6))
    plot.axis("off")

    #     cmap: str | Colormap | None = None,
    #     norm: str | Normalize | None = None,
    #     *,
    #     aspect: Literal["equal", "auto"] | float | None = None,
    #     interpolation: str | None = None,
    #     alpha: float | ArrayLike | None = None,
    #     vmin: float | None = None,
    #     vmax: float | None = None,
    #     colorizer: Colorizer | None = None,
    #     origin: Literal["upper", "lower"] | None = None,
    #     extent: tuple[float, float, float, float] | None = None,
    #     interpolation_stage: Literal["data", "rgba", "auto"] | None = None,
    #     filternorm: bool = True,
    #     filterrad: float = 4.0,
    #     resample: bool | None = None,
    #     url: str | None = None,
    #     data=None,
    #     **kwargs,

    # cmap=cmap,
    # interpolation="nearest", origin="upper"
    plot.imshow(disp, )
    # plt.colorbar(fraction=0.046, pad=0.04)
    plot.show()
    plot.pause(0.1)

    return img  # uint16, 0..4095


